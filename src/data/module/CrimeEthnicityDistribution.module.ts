class CrimeEthnicityDistributionModule {
    Asia: number
    Black: number
    Mixed: number
    White: number
    Other: number

    constructor(
        Asia: number,
        Black: number,
        Mixed: number,
        White: number,
        Other: number,
    ) {
        this.Asia = Asia
        this.Black = Black
        this.Mixed = Mixed
        this.White = White
        this.Other = Other
    }

}

export default CrimeEthnicityDistributionModule